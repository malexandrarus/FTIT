package com.ftit.thirdassignment;

public class Calculator {

    public int sumOfTwoNumbers(int x, int y) {
        return x + y;
    }

    public double divisionOfTwoNumbers(double x, double y) {
        return x / y;
    }

    public double multiplicationOfTwoNumbers(double x, double y) {
        return x * y;
    }

    public int subtractionOfTwoNumbers(int x, int y) {

        return x - y;
    }

    public double averageBetweenThreeNumbers(double x, double y, double z) {
        return (x + y + z)/3;
    }

    public int moduloOfTwoNumbers(int x, int y) {
        return x % y;
    }
}
